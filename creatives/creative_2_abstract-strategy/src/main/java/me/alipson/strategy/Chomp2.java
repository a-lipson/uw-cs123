    @Override
    public String toString() {
        String result = "";
        for (int x = 0; x < getWidth(); x++) {
            for (int y = 0; y < getHeight(); y++) {
                result += getBoard()[x][y] + " ";
            }
            result += "\n";
        }

        return result;
    }
